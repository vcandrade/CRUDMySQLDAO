    private void atualizarTabela() {

        try {

            ArrayList<Aluno> alunos = new AlunoDAO().listarAlunos();

            DefaultTableModel modeloTabela = (DefaultTableModel) tblAlunos.getModel();
            modeloTabela.fireTableDataChanged();
            modeloTabela.setRowCount(0);

            for (Aluno aluno : alunos) {

                String[] data = {"" + aluno.getRa(), "" + aluno.getNome(), "" + aluno.getCurso(), "" + aluno.getPeriodo(), "" + aluno.getCoeficiente(), "" + aluno.getSituacao()};
                modeloTabela.addRow(data);
            }

        } catch (ClassNotFoundException ex) {

            Logger.getLogger(AlunoWindow.class.getName()).log(Level.SEVERE, null, ex);

        } catch (SQLException ex) {

            Logger.getLogger(AlunoWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

