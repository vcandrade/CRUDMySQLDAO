    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        try {

            Aluno aluno = new Aluno();

            aluno.setRa(Integer.parseInt(this.txtRa.getText()));
            aluno.setNome(this.txtNome.getText());
            aluno.setCurso(this.cbxCurso.getSelectedItem().toString());
            aluno.setPeriodo(Integer.parseInt(this.spnPeriodo.getValue().toString()));
            aluno.setCoeficiente(Double.parseDouble(this.txtCoeficiente.getText()));
            aluno.setSituacao(this.cbxSituacao.getSelectedItem().toString());

            new AlunoDAO().cadastrarAluno(aluno);

            this.atualizarTabela();
            this.limparComponentes();

        } catch (ClassNotFoundException ex) {

            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);

        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCadastrarActionPerformed

